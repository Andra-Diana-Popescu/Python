# Booleans can have two values: True and False

my_boolean = True
print(my_boolean)

print(2 == 3)

print("hello" == "hello")

# Comparison

x = 7

print(x != 8)
print(x > 5)
print(x < 2)
print(x >= 7)
print(x <= 7)