# Write a program to calculate the number of hours in 888 minutes.

# Hint: There are 24 hours in a day, 60 minutes in an hour, and 60 seconds in a minute.
#       Use the print() statement to output the result.

################################################################################

# Solution:

print( 888 // 60)
print( 888 % 60)

# minutes = 888 minutes
# hours = 60  hours

# print (minutes // hours)  floor division
# print (minutes % hours)   modulo operator